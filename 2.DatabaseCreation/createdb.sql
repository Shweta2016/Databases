/* CREATE TABLE QUERIES */

CREATE TABLE PERSON (PERSON_ID VARCHAR2(10) NOT NULL PRIMARY KEY, NAME VARCHAR2(50) NOT NULL, BIRTHDATE DATE, GENDER VARCHAR2(10) NOT NULL, BIRTHPLACE VARCHAR2(20), PERSON_TYPE VARCHAR2(10) NOT NULL, CHECK (GENDER IN ('M', 'F')), CHECK (PERSON_TYPE IN('Actor', 'Director')) );

CREATE TABLE MOVIE (SERIAL_NO VARCHAR2(10) NOT NULL PRIMARY KEY, TITLE VARCHAR2(50) NOT NULL, RELEASE_YEAR NUMBER(4), DIRECTOR VARCHAR2(10) NOT NULL, FOREIGN KEY (DIRECTOR) REFERENCES PERSON (PERSON_ID) ON DELETE SET NULL); 

CREATE TABLE GENRE (GENRE_ID VARCHAR2(10) NOT NULL PRIMARY KEY, GENRE VARCHAR2(10) NOT NULL);

CREATE TABLE MOVIE_GENRE (MOVIE_ID VARCHAR2(10) NOT NULL, GENRE_ID VARCHAR2(10) NOT NULL, FOREIGN KEY (MOVIE_ID) REFERENCES MOVIE (SERIAL_NO) ON DELETE CASCADE, FOREIGN KEY (GENRE_ID) REFERENCES GENRE (GENRE_ID) ON DELETE CASCADE);

CREATE TABLE ROLES (MOVIE_ID VARCHAR2(10) NOT NULL, PERSON_ID VARCHAR2(10) NOT NULL, ROLE VARCHAR2(20) NOT NULL, FOREIGN KEY (MOVIE_ID) REFERENCES MOVIE (SERIAL_NO) ON DELETE CASCADE, FOREIGN KEY (PERSON_ID) REFERENCES PERSON (PERSON_ID) ON DELETE CASCADE);

CREATE TABLE IMDB_USER (USER_ID VARCHAR2(10) NOT NULL PRIMARY KEY, EMAIL VARCHAR2(50) NOT NULL, FNAME VARCHAR2(20) NOT NULL, LNAME VARCHAR2(20) NOT NULL, BIRTHDATE DATE, BIRTHPLACE VARCHAR2(10), GENDER VARCHAR2(10) NOT NULL, CHECK (GENDER IN ('M', 'F')));

CREATE TABLE REVIEW (REVIEW_ID VARCHAR2(10) NOT NULL, MOVIE_ID VARCHAR2(10) NOT NULL, AUTHOR VARCHAR2(10) NOT NULL, FOREIGN KEY (MOVIE_ID) REFERENCES MOVIE (SERIAL_NO) ON DELETE CASCADE, FOREIGN KEY (AUTHOR) REFERENCES IMDB_USER (USER_ID) ON DELETE CASCADE, RATING NUMBER, VOTES NUMBER, PUBLISH_DATE TIMESTAMP WITH TIME ZONE, CHECK (VOTES >= 0), CHECK (RATING >= 0 AND RATING <= 10));

